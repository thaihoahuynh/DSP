void oled_scroll_tick()
{
    Int16 col, idx;
    
    EZDSP5535_OSD9616_send(0x00,0x00); 
    EZDSP5535_OSD9616_send(0x00,0x10); 
    EZDSP5535_OSD9616_send(0x00,0xb0+0);

    for(col = 0; col < 128; col++) 
    {
        idx = (current_offset + col + MSG_LEN) % MSG_LEN; 
        EZDSP5535_OSD9616_send(0x40, scroll_msg[idx]);
    }

    current_offset -= 2; 
    
    while (current_offset < 0) 
    {
        current_offset += MSG_LEN;
    }
}